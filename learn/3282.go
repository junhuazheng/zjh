//源码就是指编写的最原始程序的代码。运行的软件是要经过编写的，程序员编写程序的过程中需要他们的“语言”
//音乐家用五线谱和音符，建筑师用图纸和笔，拿程序员的工作语言就是“源码”
//人们平时使用软件时，就是程序吧“源码”翻译成我们可直观的形式表现出来供我们使用的
