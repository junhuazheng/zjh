//GOPATH是GO语言中使用的一个环境变量，它使用绝对路径提供项目的工作目录

//工作目录是一个工程开发的相对参考目录
//好比当你要在公司编写一套服务器代码，你的工作所包含的桌面、计算机及椅子就是你的工作区
//工作区的概念与工作目录的概念也是类似的。
//如果不适用工作目录的概念，很多人开发时，每个人有一套自己的目录结构，读取配置文件的位置不同意
//输出的二进制运行文件也不同意，这样会导致开发的标准不同意，影响开发效率


//GOTAPH适合处理大量Go语言源码、多个包组合而成的复杂工程。

